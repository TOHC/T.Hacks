(function($) {

	skel
		.breakpoints({
			xlarge:	'(max-width: 1680px)',
			large:	'(max-width: 1280px)',
			medium:	'(max-width: 980px)',
			small:	'(max-width: 736px)',
			small2: '(max-width: 500px)',
			xsmall:	'(max-width: 480px)'
		});

	$(function() {

		var	$window = $(window),
			$body = $('body'),
			$wrapper = $('#page-wrapper'),
			$banner = $('#banner'),
			$header = $('#header');

		//Disable animations/transitions until the page has loaded.
			/*$body.addClass('is-loading');

			$window.on('load', function() {
				window.setTimeout(function() {
					$body.removeClass('is-loading');
				}, 100);
			});*/

		// Mobile?
			if (document.body.clientWidth < 500)
				$body.addClass('is-mobile');
			else
				skel
					.on('-small2 !small2', function() {
						$body.removeClass('is-mobile');
					})
					.on('+small2', function() {
						$body.addClass('is-mobile');
					});

		// Fix: Placeholder polyfill.
			$('form').placeholder();

		// Prioritize "important" elements on medium.
			skel.on('+medium -medium', function() {
				$.prioritize(
					'.important\\28 medium\\29',
					skel.breakpoint('medium').active
				);
			});

		// Scrolly.
			$('.scrolly')
				.scrolly({
					speed: 1500,
					offset: $header.outerHeight()
				});

		// Header.
			if (skel.vars.IEVersion < 9)
				$header.removeClass('alt');

			if ($banner.length > 0
			&&	$header.hasClass('alt')) {

				$window.on('resize', function() { $window.trigger('scroll'); });

				$banner.scrollex({
					bottom:		$header.outerHeight() + 1,
					terminate:	function() { $header.removeClass('alt'); },
					enter:		function() { $header.addClass('alt'); },
					leave:		function() { $header.removeClass('alt'); }
				});

			}

	});
	
    $("#title").fadeIn(800,function(){
        $("#titleDesc").fadeTo(800,1,function(){
            $("#register").fadeTo(800,1);
        });
    });

    if(document.body.clientWidth < 500){
        $("#title").css("font-size",30);
        $("#titleDesc").css("font-size",14);
        $("#titleDesc").css("margin-top",20);
        $("#register").css("margin-top",50);
    }
})(jQuery);
